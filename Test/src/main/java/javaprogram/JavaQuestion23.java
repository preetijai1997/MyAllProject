package javaprogram;

public class JavaQuestion23 {

	public static void main(String[] args) {
		//Enhance loop in java Program
		
		int[] a= {2,3,4,5,2,6,0,9};
		
		for(int val: a)
		{
			System.out.println("Array element are : "+ val);
		}
		System.out.println(" ");
		
		//For String
		String[] language= {"C","C++","Python","Java"};
		
		for(String name: language)
		{
			System.out.println(name);
		}

	}

}
